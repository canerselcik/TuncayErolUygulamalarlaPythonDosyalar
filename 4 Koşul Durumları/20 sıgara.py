# Odadaki kişilerin isimlerini kontrol eden fonksiyon
def odada_sigara_icecekmiyiz():
    iso_var_mi = input("Odada İso var mı? (E/H): ").lower()
    kursat_var_mi = input("Odada  Kürşat var mı? (E/H): ").lower()
    #KÜRŞAT SİGARA İÇMİYOR DİYE BELİRTTİM

    if (iso_var_mi == "e" or kursat_var_mi == "e"):
        print("Odadaki sigara içmeyen kişi olduğu için bu odada sigara içilmez.")
    else:
        print("Bu odada sigara içebilirsiniz.")

# Fonksiyonu çağırma
odada_sigara_icecekmiyiz()
